package sample;

public class ExampleClass {

    private double pow(double x, double p){
        return Math.pow(x,p);
    }

    private String upperCase(String s){
        return s.toUpperCase();
    }

    private String concat(String s1, String s2){
        return s1.concat(s2);
    }

    private double oppositeNumber(double number){
        return 1.0 / number;
    }

}
